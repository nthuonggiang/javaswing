    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String maSV= txtMaSV.getText();
        String hoTen= txtHoTen.getText();
        String ngaySinh= txtNgaySinh.getText();
        boolean gioiTinh= (cbGioiTinh.getSelectedItem().toString().equals("Nam")) ? true : false;
        Double diemTB= Double.parseDouble(txtDiemTB.getText());
        String donVi=txtDonVi.getText();
        Integer Luong = Integer.parseInt(txtLuong.getText());
        Integer hocPhi= Integer.parseInt(txtHocPhi.getText());
        
        if(diemTB < 0 || diemTB >10)
        {
            JOptionPane.showMessageDialog(null,"Nhap diem tu 0 den 10");
            return ;
        }
        
        if(rbSVATTT.isSelected())
        {
            System.out.println("La sinh vien ATTT");
        }
        if(rbSVMM.isSelected())
        {
            System.out.println("La sinh vien mat ma");
        }
        
        
       
        JOptionPane.showMessageDialog(null,"Nhap thanh cong");
        if(rbSVATTT.isSelected())
        {
            
            SinhVienATTT sv = new SinhVienATTT(hocPhi, maSV, hoTen, ngaySinh, gioiTinh, diemTB);
            listSinhVien1.add(sv);
            System.out.println(sv.toString());
        }
        if(rbSVMM.isSelected())
        {
            SinhVienMatMa sv1= new SinhVienMatMa(donVi, Luong, maSV, hoTen, ngaySinh, gioiTinh, diemTB);
            listSinhVien2.add(sv1);
            System.out.println(sv1.toString());
        }
    }//GEN-LAST:event_btnThemActionPerformed

