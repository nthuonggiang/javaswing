    public boolean ghiFileBinary(Object obj,String tenFile)
    {
        try {
            File f = new File(tenFile);
            FileWriter fw= new FileWriter(f);
            BufferedWriter bw=new BufferedWriter(fw);
            bw.write(obj.toString());
            bw.close();
            fw.close();
            
            return true;
        } catch (Exception e) {
            return false;
        }
    }
